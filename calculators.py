def калькулятор_вклада(сумма, ставка, месяцев):
    месячная_ставка = ставка / 12 / 100
    итог = сумма * (1 + месячная_ставка) ** месяцев
    return round(итог, 2)

def калькулятор_кредита(сумма, годовая_ставка, месяцев):
    месячная_ставка = годовая_ставка / 12 / 100
    if месячная_ставка == 0:
        платёж = сумма / месяцев
    else:
        платёж = сумма * (месячная_ставка * (1 + месячная_ставка) ** месяцев) / (((1 + месячная_ставка) ** месяцев) - 1)
    итого = платёж * месяцев
    return round(платёж, 2), round(итого, 2)